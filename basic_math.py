def main():
    print("9+3={}".format(9 + 3))
    print("6-10={}".format(6 - 10))
    print("5*5={}".format(5 * 5))
    print("10/3={}".format(10 / 3))
    print("7+10*7={}".format(7+10*7))
    print("8**4={}".format(8**4))  # exponent
    print("100**0.5={}".format(100**0.5))  # square root


if __name__ == "__main__":
    main()
